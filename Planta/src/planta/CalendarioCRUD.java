    private void botonAnadirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAnadirActionPerformed
        String nombreAnadirCalendario = anadirNombreTextF.getText();
        int opcionTipoPago;
        switch (anadirTipoComboBox.getSelectedIndex()) {
            case 0:
                opcionTipoPago = 1;
                break;
            case 1:
                opcionTipoPago = 2;
                break;
            case 2:
                opcionTipoPago = 3;
                break;
            default:
                throw new AssertionError();
        }
        
        if (!nombreAnadirCalendario.equals("")) {
            String mensajeSalida = anadirCalendario(nombreAnadirCalendario, opcionTipoPago);
            JOptionPane.showMessageDialog(this, 
                    mensajeSalida, 
                    "Mensaje", JOptionPane.INFORMATION_MESSAGE);
            actualizarCombosBox();
        } else {
            JOptionPane.showMessageDialog(this, 
                    "Favor ingresar un nombre para el calendario", 
                    "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_botonAnadirActionPerformed

