    private void botonModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonModificarActionPerformed
        int idSeleccionado = Integer.parseInt(opcionesConsulta[0].split(",")[comboModificarNombreCalendario.getSelectedIndex()]);
        int opcionTipoPago;
        switch (modificarTipoComboBox.getSelectedIndex()) {
            case 0:
                opcionTipoPago = 1;
                break;
            case 1:
                opcionTipoPago = 2;
                break;
            case 2:
                opcionTipoPago = 3;
                break;
            default:
                throw new AssertionError();
        }
        
        String mensajeSalida = modificarCalendario(idSeleccionado, opcionTipoPago);
        JOptionPane.showMessageDialog(this, 
                    mensajeSalida, 
                    "Mensaje", JOptionPane.INFORMATION_MESSAGE);
        actualizarCombosBox();
    }//GEN-LAST:event_botonModificarActionPerformed

