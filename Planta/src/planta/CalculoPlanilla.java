    private void BTNConsultarPlanillaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTNConsultarPlanillaActionPerformed
        // TODO add your handling code here:
        int idSeleccionado = Integer.parseInt(opcionesConsultaCalendario[0].split(",")[comboBoxCalendario.getSelectedIndex()]);
        String fechaInicial;
        try {
            fechaInicial = opcionesConsultaFechasDPago[1].split(",")[comboBoxFechasDPago.getSelectedIndex() - 1];
        } catch (IndexOutOfBoundsException e) {
            fechaInicial = opcionesConsultaFechasDPago[1].split(",")[opcionesConsultaFechasDPago[1].split(",").length - 1];
            fechaInicial = "2022" + fechaInicial.substring(4);
        }
        String fechaFinal = (String) comboBoxFechasDPago.getSelectedItem();
        if (!PlantaFunciones.verificarExtrasPendientesPf(idSeleccionado, fechaInicial, fechaFinal)) {
            JOptionPane.showMessageDialog(this, "Para acceder a esta funciÃ³n primero debe aproba o rechazar las revisiones de horas extra pendientes", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        idCalendario = idSeleccionado;
        fechaInicialDPagoSeleccionada = fechaInicial;
        fechaFinalDPagoSeleccionada = fechaFinal;
        
        // consultar planilla
        lineasSalida = PlantaFunciones.calcularPlanilla(idCalendario, fechaInicial, fechaFinal);
        
        String[] elementosLinea;
        
        String resultadoTextArea = "IdEmpleado \t Fecha Pago \t Salario Bruto \t Salario Neto \t Planta \t Tipo Pago \n";
        for (int i = 1; i < lineasSalida.length; i++) {
            elementosLinea = lineasSalida[i].split("\t");
            if (PlantaFunciones.verificarIdEmpleadoEnPlanilla(Integer.parseInt(elementosLinea[0]), fechaFinalDPagoSeleccionada)) {
                int idEmpleadoIns = Integer.parseInt(elementosLinea[0]);
                String fechaDPago = fechaFinalDPagoSeleccionada;
                float montoPagadoBruto = Float.parseFloat(elementosLinea[1]);
                float montoPagadoNeto = Float.parseFloat(elementosLinea[2]);
                
                // cambiar por app
                String planta = "Guayabo";
                int tipoDPago = PlantaFunciones.consultarTipoDPagoNUMPorCalendario(idCalendario);
                
                resultadoTextArea += idEmpleadoIns + "\t" + fechaDPago + "\t" + montoPagadoBruto + "\t" + montoPagadoNeto + "\t" + planta + "\t" + tipoDPago + "\n";
                PlantaFunciones.InsertarPlanilla(idEmpleadoIns, fechaDPago, montoPagadoBruto, montoPagadoNeto, planta, tipoDPago);
            }
        }
        // INSERTAR PLANILLA
        
        // muestra en text area
        txtAreaConsulta.setText(resultadoTextArea);
    }//GEN-LAST:event_BTNConsultarPlanillaActionPerformed

    private void BTNAprobarPlanillaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTNAprobarPlanillaActionPerformed
        // TODO add your handling code here:
        if (idCalendario == -1 || lineasSalida == null) {
            JOptionPane.showMessageDialog(this, "Favor consultar planilla", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        int tipoDPago = PlantaFunciones.consultarTipoDPagoNUMPorCalendario(idCalendario);
        PlantaFunciones.generarCSV(fechaFinalDPagoSeleccionada, tipoDPago);
        
        idCalendario = -1;
        fechaInicialDPagoSeleccionada = "";
        fechaFinalDPagoSeleccionada = "";
        txtAreaConsulta.setText("");
        JOptionPane.showMessageDialog(this, "Proceso realizado de manera exitosa", "!!", JOptionPane.INFORMATION_MESSAGE);
        return;
    }//GEN-LAST:event_BTNAprobarPlanillaActionPerformed

    private void BTNRechazarPlanillaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTNRechazarPlanillaActionPerformed
        // TODO add your handling code here:
        try {
            if (idCalendario == -1 || lineasSalida == null) {
                JOptionPane.showMessageDialog(this, "Favor consultar planilla", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            
            String[] elementosLinea;
            for (int i = 1; i < lineasSalida.length; i++) {
                elementosLinea = lineasSalida[i].split("\t");
                int idEmpleadoEli = Integer.parseInt(elementosLinea[0]);
                String fechaDPago = fechaFinalDPagoSeleccionada;
                PlantaFunciones.EliminarDPlanilla(idEmpleadoEli, fechaDPago);
            }
            
            idCalendario = -1;
            fechaInicialDPagoSeleccionada = "";
            fechaFinalDPagoSeleccionada = "";
            txtAreaConsulta.setText("");
            JOptionPane.showMessageDialog(this, "Proceso realizado de manera exitosa", "!!", JOptionPane.INFORMATION_MESSAGE);
        } catch (Exception e) {
            System.out.println(e);
        }
    }//GEN-LAST:event_BTNRechazarPlanillaActionPerformed

    private void actualizarCombosBoxCalendario(){
        comboBoxCalendario.removeAllItems();
        try {
            opcionesConsultaCalendario = PlantaFunciones.getNombresCalendarios();
            String[] opciones = opcionesConsultaCalendario[1].split(",");
            for (int i = 0; i < opciones.length; i++) {

                comboBoxCalendario.addItem(opciones[i]);
            }
        } catch (Exception e) {
            System.out.println(e);
        }
    }

    private void actualizarComboBoxFechasDPago(){
        comboBoxFechasDPago.removeAllItems();
        try {
            int idSeleccionado = Integer.parseInt(opcionesConsultaCalendario[0].split(",")[comboBoxCalendario.getSelectedIndex()]);
            opcionesConsultaFechasDPago = PlantaFunciones.TraerDiasDPago(idSeleccionado);
            String[] opciones = opcionesConsultaFechasDPago[1].split(",");
            for (int i = 0; i < opciones.length; i++) {

                comboBoxFechasDPago.addItem(opciones[i]);
            }
        } catch (Exception e) {
            System.out.println(e);
        }
    }
    
