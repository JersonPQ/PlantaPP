    private void actualizarCombosBoxCalendario(){
        comboConsultarCalendario.removeAllItems();
        comboAnadirCalendario.removeAllItems();
        comboModificarCalendario.removeAllItems();
        comboEliminarCalendario.removeAllItems();
        try {
            opcionesConsultaCalendario = getNombresCalendarios();
            String[] opciones = opcionesConsultaCalendario[1].split(",");
            for (int i = 0; i < opciones.length; i++) {
                comboConsultarCalendario.addItem(opciones[i]);
                comboAnadirCalendario.addItem(opciones[i]);
                comboModificarCalendario.addItem(opciones[i]);
                comboEliminarCalendario.addItem(opciones[i]);
            }
        } catch (Exception e) {
            System.out.println("Error actualizar combos calendario: " + e);
        }
    }
    
    
    
