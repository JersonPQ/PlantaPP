    private void botonAnadirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAnadirActionPerformed
        int idSeleccionado = Integer.parseInt(opcionesConsultaCalendario[0].split(",")[comboAnadirCalendario.getSelectedIndex()]);
        int lunes = 0;
        if(Lunes.isSelected()){
            lunes = 2;
        }
        int martes = 0;
        if(Martes.isSelected()){
            martes = 3;
        }
        int miercoles = 0;
        if(Miercoles.isSelected()){
            miercoles = 4;
        }
        int jueves = 0;
        if(Jueves.isSelected()){
            jueves = 5;
        }
        int viernes = 0;
        if(Viernes.isSelected()){
            viernes = 6;
        }
        int sabado = 0;
        if(Sabado.isSelected()){
            sabado = 7;
        }
        int domingo = 0;
        if(Domingo.isSelected()){
            domingo = 1;
        }
        
        String HEntrada = HEntradaTF.getText();
        Pattern pattern = Pattern.compile("^(([0-1]\\d)|(2[0-3]))(:[0-5]\\d){2}$");
        Matcher mat = pattern.matcher(HEntrada);
        if(!mat.matches()){
        JOptionPane.showMessageDialog(this, 
                    "LA HORA INGRESADA DEBE SER VÁLIDA Y CUMPLIR CON EL FORMATO HH:MM:SS Y EN FORMATO 24 HORAS", 
                    "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        String HSalida = HSalidaTF.getText();
        mat = pattern.matcher(HSalida);
        if(!mat.matches()){
        JOptionPane.showMessageDialog(this, 
                    "LA HORA INGRESADA DEBE SER VÁLIDA Y CUMPLIR CON EL FORMATO HH:MM:SS Y EN FORMATO 24 HORAS", 
                    "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if(PlantaFunciones.HoraCSentido(HEntrada, HSalida) == false){
            JOptionPane.showMessageDialog(this, 
                    "LA HORA DE ENTRADA DEBE SER ANTERIOR A LA HORA DE SALIDA", 
                    "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if(PlantaFunciones.verificarDiasTrabajados(idSeleccionado) == false){
            JOptionPane.showMessageDialog(this, 
                    "YA HAY DÍAS TRABAJADOS REGISTRADOS EN ESTE CALENDARIO, PARA REGISTRAR NUEVOS DÍAS DEBE ELIMINAR LOS DÍAS YA REGISTRADOS PRIMERO.", 
                    "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        boolean bandera = PlantaFunciones.insertarDiasTrabajados(idSeleccionado, lunes, martes, miercoles, jueves, viernes, sabado, domingo, HEntrada, HSalida);
        if(bandera == true){
            JOptionPane.showMessageDialog(this, 
                    "PROCESO REALIZADO DE MANERA EXITOSA.", 
                    "Error", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        else{
            JOptionPane.showMessageDialog(this, 
                    "PROCESO NO REALIZADO", 
                    "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
    }//GEN-LAST:event_botonAnadirActionPerformed

